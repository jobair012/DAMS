package beans;

public class SpecializedSectionBean {

	private String speciality;

	public SpecializedSectionBean() {
	}

	public SpecializedSectionBean(String speciality) {
		this.speciality = speciality;
	}

	public String getSpeciality() {
		return speciality;
	}

	public void setSpeciality(String speciality) {
		this.speciality = speciality;
	}

}
